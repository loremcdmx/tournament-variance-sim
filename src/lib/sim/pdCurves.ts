/**
 * Expand an anchor list to a per-place fractions array of length `paid`,
 * following PD's `Distribution.generate` rule: anchor `(p, f)` fills
 * places `prev+1 .. p` with `f`. Truncates if native > requested.
 */
function expandAnchorCurve(
  anchors: readonly (readonly [number, number])[],
  paid: number,
): number[] {
  const native = anchors[anchors.length - 1][0];
  const len = Math.min(paid, native);
  const out = new Array<number>(len).fill(0);
  let prev = 0;
  for (const [p, f] of anchors) {
    const until = Math.min(p, len);
    for (let j = prev; j < until; j++) out[j] = f;
    prev = until;
    if (prev >= len) break;
  }
  return out;
}

/**
 * Pick the PrimeDope live curve for a requested paid-places count and
 * return a fractions-of-pool vector of length `paid` that sums to 1.
 *
 * Selection rule: exact native match if `paid` lands on a dropdown
 * value; otherwise the smallest curve with native paid ≥ requested,
 * truncated and renormalised. Beyond the top (paid > 700) we use the
 * largest curve and pad with zeros.
 */
export function primedopeCurveForPaid(paid: number): number[] {
  const want = Math.max(1, Math.floor(paid));
  let curve = PD_LIVE_CURVES.find((c) => c.paid === want);
  if (!curve) curve = PD_LIVE_CURVES.find((c) => c.paid >= want);
  if (!curve) curve = PD_LIVE_CURVES[PD_LIVE_CURVES.length - 1];
  const expanded = expandAnchorCurve(curve.anchors, want);
  const padded =
    expanded.length < want
      ? [...expanded, ...new Array(want - expanded.length).fill(0)]
      : expanded;
  let s = 0;
  for (const v of padded) s += v;
  if (s > 0) for (let i = 0; i < padded.length; i++) padded[i] /= s;
  return padded;
}
